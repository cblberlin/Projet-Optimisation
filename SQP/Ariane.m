function [M_0, c] = Ariane(m_e)

k1 = 0.1101; k2 = 0.1532; k3 = 0.2154;

v_e1 = 2647.2; v_e2 = 2922.4; v_e3 = 4344.3;

V = 11527; m_u = 1700;

M_0 = (k1+1) * m_e(1) + (k2+1) * m_e(2) + (k3+1) * m_e(3) + m_u;

c1 = v_e1 * log( ((k1 + 1)*m_e(1) + (k2 + 1)*m_e(2) + (k3 + 1)*m_e(3) + m_u) / (k1*m_e(1) + (k2+1)*m_e(2) + (k3+1)*m_e(3) + m_u));

c2 = v_e2 * log( ((k2 + 1)*m_e(2) + (k3 + 1)*m_e(3) + m_u) / (k2*m_e(2) + (k3+1)*m_e(3) + m_u));

c3 = v_e3 * log( ((k3 + 1)*m_e(3) + 1700)/(k3*m_e(3) + m_u) );

c = c1 + c2 + c3 - V;

end